    let day = "monday";
    switch (day) {
        case "monday":
            console.log("Today is Monday");
            break;
        case "tuesday":
            console.log("Today is Tuesday");
            break;
        case "wednesday":
            console.log("Today is Wednesday");
            break;
        default:
            console.log("Today is not Monday, Tuesday or Wednesday");
    }



