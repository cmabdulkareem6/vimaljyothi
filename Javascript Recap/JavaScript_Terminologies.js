// Javascript Terminologies

// variable : a container for storing data
// data : a value stored in a variable

// operators : symbols used to perform operations on data like +, -, *, /, %, **

// function : a block of code that performs a specific task

// object : a collection of related data and functions in key-value pairs

// array : a list of elements stored in a single variable

// loop : a block of code that is executed multiple times

// condition : a statement that evaluates to true or false

// callback : a function passed as an argument to another function

// promise : an object that represents the result of an asynchronous operation

// error : an object that represents an error

// callback hell : a situation where multiple nested callbacks are used to handle asynchronous operations

// promise hell : a situation where multiple nested promises are used to handle asynchronous operations

// try...catch : a block of code that is executed when an error occurs

// try...catch...finally : a block of code that is executed when an error occurs or finally


